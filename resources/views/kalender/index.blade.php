    <script>
        // Variabel untuk menyimpan data events
        let eventsData = [];

        // Coba parsing data events dari backend
        try {
            eventsData = {!! json_encode($events->map(function ($event) {
    return [
        'id' => $event->id,
        'title' => $event->title,
        'description' => $event->description,
        'event_date' => $event->event_date->format('Y-m-d'),
        'start_time' => $event->formatted_start_time,
        'end_time' => $event->formatted_end_time,
        'location' => $event->location,
        'category' => $event->category,
        'category_display' => $event->category_display,
        'url' => route('kalender.show', $event->id),
        'created_by' => $event->created_by,
        'creator_name' => $event->creator->name ?? 'Unknown'
    ];
})) !!};
        } catch (e) {
            console.error('Error parsing events data:', e);
            eventsData = [];
        }

        let currentDate = new Date({{ $year ?? date('Y') }}, {{ ($month ?? date('n')) - 1 }}, 1);
        let currentView = 'calendar';

        // Initialize calendar
        document.addEventListener('DOMContentLoaded', function () {
            generateCalendar();
        });

        function generateCalendar() {
            const year = currentDate.getFullYear();
            const month = currentDate.getMonth();

            // Update month display
            document.getElementById('currentMonth').textContent =
                new Date(year, month).toLocaleDateString('id-ID', { month: 'long', year: 'numeric' });

            // Get first day of month and number of days
            const firstDay = new Date(year, month, 1).getDay();
            const daysInMonth = new Date(year, month + 1, 0).getDate();

            const calendarDays = document.getElementById('calendarDays');
            calendarDays.innerHTML = '';

            // Add empty cells for days before month starts
            for (let i = 0; i < firstDay; i++) {
                const emptyDay = document.createElement('div');
                emptyDay.className = 'calendar-day empty';
                calendarDays.appendChild(emptyDay);
            }

            // Add days of the month
            for (let day = 1; day <= daysInMonth; day++) {
                const dayElement = document.createElement('div');
                dayElement.className = 'calendar-day';

                const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
                const dayEvents = eventsData.filter(event => event.event_date === dateStr);

                // Check if today
                const today = new Date();
                if (year === today.getFullYear() && month === today.getMonth() && day === today.getDate()) {
                    dayElement.classList.add('today');
                }

                dayElement.innerHTML = `
                    <div class="day-number">${day}</div>
                    ${dayEvents.length > 0 ? `<div class="event-indicator">${dayEvents.length}</div>` : ''}
                `;

                if (dayEvents.length > 0) {
                    dayElement.classList.add('has-events');
                    dayElement.addEventListener('click', () => showDayEvents(dateStr, dayEvents));
                }

                calendarDays.appendChild(dayElement);
            }
        }

        function previousMonth() {
            currentDate.setMonth(currentDate.getMonth() - 1);
            loadEventsForMonth();
            generateCalendar();
        }

        function nextMonth() {
            currentDate.setMonth(currentDate.getMonth() + 1);
            loadEventsForMonth();
            generateCalendar();
        }

        function loadEventsForMonth() {
            const year = currentDate.getFullYear();
            const month = currentDate.getMonth() + 1;
            window.location.href = `{{ route('kalender.index') }}?year=${year}&month=${month}`;
        }

        function switchView(view) {
            const calendarView = document.getElementById('calendarView');
            const listView = document.getElementById('listView');
            const toggleBtns = document.querySelectorAll('.toggle-btn');

            toggleBtns.forEach(btn => btn.classList.remove('active'));

            if (view === 'calendar') {
                calendarView.style.display = 'block';
                listView.style.display = 'none';
                toggleBtns[0].classList.add('active');
            } else {
                calendarView.style.display = 'none';
                listView.style.display = 'block';
                toggleBtns[1].classList.add('active');
            }

            currentView = view;
        }

        function showDayEvents(date, dayEvents) {
            const modal = document.getElementById('eventModal');
            const modalTitle = document.getElementById('modalTitle');
            const modalBody = document.getElementById('modalBody');

            modalTitle.textContent = `Kegiatan ${new Date(date).toLocaleDateString('id-ID', {
                day: 'numeric',
                month: 'long',
                year: 'numeric'
            })}`;

            modalBody.innerHTML = dayEvents.map(event => `
                <div class="modal-event">
                    <h4>${event.title}</h4>
                    <p>${event.description || 'Tidak ada deskripsi'}</p>
                    <div class="modal-event-meta">
                        <span><i class="icon">üïê</i> ${event.start_time}${event.end_time ? ' - ' + event.end_time : ''}</span>
                        <span><i class="icon">üìç</i> ${event.location}</span>
                        ${event.category ? `<span><i class="icon">üìÇ</i> ${event.category_display}</span>` : ''}
                        <span><i class="icon">üë§</i> ${event.creator_name}</span>
                    </div>
                    <div class="modal-event-actions">
                        <a href="${event.url}" class="btn-view">Lihat Detail</a>
                        ${({{ auth()->user()->role === 'admin' ? 'true' : 'false' }} || event.created_by === {{ auth()->id() }}) ?
                    `<a href="/kalender/${event.id}/edit" class="btn-edit">Edit</a>` : ''}
                    </div>
                </div>
            `).join('');

            modal.style.display = 'flex';
        }

        function closeModal() {
            document.getElementById('eventModal').style.display = 'none';
        }

        // Close modal when clicking outside
        window.onclick = function (event) {
            const modal = document.getElementById('eventModal');
            if (event.target === modal) {
                modal.style.display = 'none';
            }
        }
    </script>
